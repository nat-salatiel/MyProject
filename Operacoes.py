#Realizando as quatro operações básicas da matemática

x = int(input("Digite o número 1: "))
y = int(input("Digite o número 2: "))

soma = x + y 
subtracao = x - y
divisao = x/y
multiplicacao = x * y

print(f"Soma: {soma}")
print(f"Subtração: {subtracao}")
print(f"Divisão: {divisao}")
print(f"Multiplicação: {multiplicacao}")