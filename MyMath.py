#Demonstração do uso de operadores
#Criar um programa que faça o somatório de 3 valores e tire a sua média

x = int(input("Digite o valor 1: "))
y = int(input("Digite o valor 2: "))
z = int(input("Digite o valor 3: "))

soma = x + y + z
media = soma / 3

print("O resultado da soma é: ", soma)
print(f"O resultado da média é: {media}")